f1 = lambda: 10 + 20                # Функция без параметров
f2 = lambda x, y: x + y             # Функция с двумя параметрами
f3 = lambda x, y, z: x + y + z      # Функция с тремя параметрами
print(f1())                         # Выведет: 30
print(f2(5, 10))                    # Выведет: 15
print(f3(5, 10, 30))                # Выведет: 45
